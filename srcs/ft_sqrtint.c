int		ft_sqrtint(int n)
{
	int		a;
	int		b;

	if (n < 1)
		return ((n == 0) ? 0 : -1);
	a = n;
	b = 1;
	while (a > b)
	{
		a = (a + b) / 2;
		b = n / a;
	}
	if (a * a > n)
		return (a - 1);
	return (a);
}
